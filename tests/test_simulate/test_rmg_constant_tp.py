def test_csv_parser_matches_adapter():
    """The standalone CSV parser should produce the same sa_dict as the adapter."""
    adapter = _make_adapter(observable_list=['OH', 'H'])
    _mock_simulate(adapter)
    sa_dict_yaml = adapter.get_sa_coefficients()

    sa_dict_csv = rmg_sa_csvs_to_sa_dict(
        os.path.join(adapter.paths['SA'], 'solver'),
        chem_annotated_path=adapter.paths['chem annotated'],
    )

    # Time should match
    np.testing.assert_array_almost_equal(sa_dict_csv['time'][0], sa_dict_yaml['time'][0])

    # Observables should match
    assert set(sa_dict_csv['kinetics'][0].keys()) == set(sa_dict_yaml['kinetics'][0].keys())
    assert set(sa_dict_csv['thermo'][0].keys()) == set(sa_dict_yaml['thermo'][0].keys())

    # Kinetics values should match
    for obs in sa_dict_yaml['kinetics'][0]:
        yaml_params = sa_dict_yaml['kinetics'][0][obs]
        csv_params = sa_dict_csv['kinetics'][0][obs]
        assert set(csv_params.keys()) == set(yaml_params.keys()), \
            f'Kinetics key mismatch for {obs}: csv={set(csv_params.keys())} yaml={set(yaml_params.keys())}'
        for param in yaml_params:
            np.testing.assert_array_almost_equal(
                csv_params[param], yaml_params[param],
                err_msg=f'kinetics/{obs}/{param} mismatch')

    # Thermo values should match
    for obs in sa_dict_yaml['thermo'][0]:
        yaml_params = sa_dict_yaml['thermo'][0][obs]
        csv_params = sa_dict_csv['thermo'][0][obs]
        assert set(csv_params.keys()) == set(yaml_params.keys())
        for param in yaml_params:
            np.testing.assert_array_almost_equal(
                csv_params[param], yaml_params[param],
                err_msg=f'thermo/{obs}/{param} mismatch')


