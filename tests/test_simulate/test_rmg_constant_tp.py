# Files generated by tests that should be cleaned up (not the fixture CSVs)
_GENERATED_FILES = []


def _make_adapter(observable_list=None):
    """Helper to construct an RMGConstantTP adapter using the minimal example."""
    t3 = run_minimal(project_directory=TEST_DIR)
    t3.set_paths()
    adapter = RMGConstantTP(t3=t3.t3,
                             rmg=t3.rmg,
                             paths=t3.paths,
                             logger=t3.logger,
                             atol=t3.rmg['model']['atol'],
                             rtol=t3.rmg['model']['rtol'],
                             observable_list=observable_list or list(),
                             sa_atol=t3.t3['sensitivity']['atol'],
                             sa_rtol=t3.t3['sensitivity']['rtol'],
                             )
    return adapter


def _mock_simulate(adapter):
    """Simulate what run_rmg_sa_incore does: parse CSV fixtures and write sa_output.yml."""
    raw = parse_rmg_sa_csvs(os.path.join(adapter.paths['SA'], 'solver'))
    with open(adapter.sa_output_file, 'w') as f:
        yaml.dump(raw, f, default_flow_style=False)
    _GENERATED_FILES.append(adapter.sa_output_file)


